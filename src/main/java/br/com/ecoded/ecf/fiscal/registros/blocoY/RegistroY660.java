package br.com.ecoded.ecf.fiscal.registros.blocoY;

public class RegistroY660 {

	private final String reg = "Y660";
	private String cnpj;
	private String nome_emp;
	private String perc_pat_liq;

	public String getCnpj() {
		return cnpj;
	}

	public void setCnpj(String cnpj) {
		this.cnpj = cnpj;
	}

	public String getNome_emp() {
		return nome_emp;
	}

	public void setNome_emp(String nome_emp) {
		this.nome_emp = nome_emp;
	}

	public String getPerc_pat_liq() {
		return perc_pat_liq;
	}

	public void setPerc_pat_liq(String perc_pat_liq) {
		this.perc_pat_liq = perc_pat_liq;
	}

	public String getReg() {
		return reg;
	}

}
