package br.com.ecoded.ecf.fiscal.registros.blocoX;

public class RegistroX357 {

	private final String reg = "X357";
	private String pais;
	private String nif_cnpj;
	private String razao_social;
	private String percentual;

	public String getPais() {
		return pais;
	}

	public void setPais(String pais) {
		this.pais = pais;
	}

	public String getNif_cnpj() {
		return nif_cnpj;
	}

	public void setNif_cnpj(String nif_cnpj) {
		this.nif_cnpj = nif_cnpj;
	}

	public String getRazao_social() {
		return razao_social;
	}

	public void setRazao_social(String razao_social) {
		this.razao_social = razao_social;
	}

	public String getPercentual() {
		return percentual;
	}

	public void setPercentual(String percentual) {
		this.percentual = percentual;
	}

	public String getReg() {
		return reg;
	}

}
