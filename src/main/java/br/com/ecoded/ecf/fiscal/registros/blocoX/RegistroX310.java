package br.com.ecoded.ecf.fiscal.registros.blocoX;

public class RegistroX310 {

	private final String reg = "X310";
	private String nome;
	private String pais;
	private String vl_oper;
	private String cond_pes;

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getPais() {
		return pais;
	}

	public void setPais(String pais) {
		this.pais = pais;
	}

	public String getVl_oper() {
		return vl_oper;
	}

	public void setVl_oper(String vl_oper) {
		this.vl_oper = vl_oper;
	}

	public String getCond_pes() {
		return cond_pes;
	}

	public void setCond_pes(String cond_pes) {
		this.cond_pes = cond_pes;
	}

	public String getReg() {
		return reg;
	}

}
